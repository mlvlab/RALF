def merge_dicts(dict1, dict2):
    """
    Both dictionaries should have non-overlapping keys
    """
    combined_dict = {}
    for key in dict1.keys():
        combined_dict[key] = dict1[key]
    for key in dict2.keys():
        combined_dict[key] = dict2[key]

    return combined_dict


def get_query(cat_id):
    categories_lvis = merge_dicts(lvis_seen_train, lvis_rare)
    category = categories_lvis[cat_id]
    query = f"every {category}"
    unseen = True if cat_id in lvis_rare.keys() else False

    return query, unseen
